package com.sap.plc.backend.model.pks;

import com.sap.plc.backend.model.PrimaryKey;
import com.sap.plc.backend.util.PlcSimpleToStringStyle;
import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

import java.sql.Timestamp;

public class PlantPrimaryKey implements PrimaryKey {

    private static final long serialVersionUID = 4903834364692385171L;

    private String plantId;
    private Timestamp validFrom;

    public PlantPrimaryKey() {
    }

    public PlantPrimaryKey(String plantId, Timestamp validFrom) {
        this.plantId = plantId;
        this.validFrom = validFrom;
    }

    public String getPlantId() {
        return plantId;
    }

    public void setPlantId(String plantId) {
        this.plantId = plantId;
    }

    public Timestamp getValidFrom() {
        return validFrom;
    }

    public void setValidFrom(Timestamp validFrom) {
        this.validFrom = validFrom;
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder(17, 37)
                .append(plantId)
                .append(validFrom)
                .toHashCode();
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }

        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        PlantPrimaryKey that = (PlantPrimaryKey) o;

        return new EqualsBuilder()
                .append(plantId, that.plantId)
                .append(validFrom, that.validFrom)
                .isEquals();
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this, PlcSimpleToStringStyle.PLC_SIMPLE_STYLE)
                .append("plantId", plantId)
                .append("validFrom", validFrom)
                .toString();
    }
}