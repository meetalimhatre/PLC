package com.sap.plc.backend.model.pks;

import com.sap.plc.backend.model.PrimaryKey;
import com.sap.plc.backend.util.PlcSimpleToStringStyle;
import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

import java.sql.Timestamp;

public class MaterialPrimaryKey implements PrimaryKey {

    private static final long serialVersionUID = -6462539464311292551L;

    private String materialId;
    private Timestamp validFrom;

    public MaterialPrimaryKey() {
    }

    public MaterialPrimaryKey(String materialId, Timestamp validFrom) {
        this.materialId = materialId;
        this.validFrom = validFrom;
    }

    public String getMaterialId() {
        return materialId;
    }

    public void setMaterialId(String materialId) {
        this.materialId = materialId;
    }

    public Timestamp getValidFrom() {
        return validFrom;
    }

    public void setValidFrom(Timestamp validFrom) {
        this.validFrom = validFrom;
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder(17, 37)
                .append(materialId)
                .append(validFrom)
                .toHashCode();
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }

        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        MaterialPrimaryKey that = (MaterialPrimaryKey) o;

        return new EqualsBuilder()
                .append(materialId, that.materialId)
                .append(validFrom, that.validFrom)
                .isEquals();
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this, PlcSimpleToStringStyle.PLC_SIMPLE_STYLE)
                .append("materialId", materialId)
                .append("validFrom", validFrom)
                .toString();
    }
}